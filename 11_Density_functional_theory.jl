# ╔═╡ 887a5950-fe79-4852-8a32-3dd1f58d6709
md"""
## DFT: A non-linear eigenvalue problem

Consider a Schrödinger Hamiltonian $\mathcal{H} = - \frac12 \Delta + V$ on $L^2(\mathbb{R}^{3N})$. As discussed before, if $V$ is sufficiently nice this operator is self-adjoint with $Q(\mathcal{H}) = H^1(\mathbb{R}^{3N})$ and to get the ground state (first eigenvalue) we would seek to solve the problem
```math
\tag{1}
\min_{\Psi \in H^1(\mathbb{R}^\textcolor{red}{3N})} \frac{\langle \Psi, \mathcal{H} \Psi\rangle_{L^2(\mathbb{R}^\textcolor{red}{3N})}
}{\langle \Psi, \Psi\rangle_{L^2(\mathbb{R}^\textcolor{red}{3N})}}
```
which notably involves two integrals over the entire space $\mathbb{R}^{3N}$ as part of the inner products.

The dimension $3N$ of this problem is determined by the number $N$ of the considered particles. For materials science and chemistry one important setting is where one aims to model the **electrons in a material** or **chemical system**. In that case $N$ is equal to the number of electrons. Even for moderate systems, such as a silicon crystal, we have $N = 28$ and $3N = 84$. Therefore the integrals to be evaluated as part of (1) get very quickly very challenging.

To illustrate this point further. If we want the inner product $\langle \Psi, \Psi\rangle_{L^2(\mathbb{R}^\textcolor{red}{3N})}$ to be computed in **1 year** using a trapezoidal rule with two sampling points along every dimension, then **evaluating one sample may take at most**
"""

# ╔═╡ 7546c02d-8293-4523-9774-0400fcb3e003
let
	N = 28
	x = 1.0u"yr" / 2.0^(3N)
	uconvert(u"as", x)  # Convert to Attoseconds
end

# ╔═╡ f7dad8bf-50c0-4730-9e1b-6b0879382895
md"""
which is **impossible due to physical constraints in modern computer hardware**.

The way out to break this curse of dimensionality are **mean-field methods**. One such approach is density-functional theory (DFT). Without going into details the idea is to approximate the states $\Psi$ by antisymmetric tensor products
```math
\widetilde{\Psi}(r_1, r_2, \ldots, r_N) = \frac{1}{N!} \det \begin{pmatrix}
\psi_1(r_1) & \psi_1(r_2) & \cdots & \psi_1(r_N) \\
\psi_2(r_1) & \psi_2(r_2) & \cdots & \psi_2(r_N) \\
\vdots &  & \ddots & \vdots \\
\psi_N(r_1) & \psi_N(r_2) & \cdots & \psi_N(r_N)
\end{pmatrix}
```
where
```math
\begin{aligned}
\forall i, j: \quad r_i \in \mathbb{R}^3, \psi_i \in H^1(\mathbb{R}^{\textcolor{red}{3}}), \langle \psi_i, \psi_j \rangle_{L^2(\mathbb{R}^{\textcolor{red}{3}})} = \delta_{ij}.
\end{aligned}
```
In this field such a $\widetilde{\Psi}$ is usually called a **Slater determinant**. We additionally collect the degrees of freedom of $\widetilde{\Psi}$ in a tuple
$\Phi = (\psi_1, \psi_2, \ldots, \psi_N) \in \left(H^1(\mathbb{R}^3)\right)^N$.
"""

# ╔═╡ 9b2bc5db-716b-4e07-a931-c7a4212be13f
md"""
One natural idea would be now to insert $\widetilde{\Psi}$ into (1), i.e. minimising $\langle \widetilde{\Psi}, \mathcal{H} \widetilde{\Psi}\rangle_{L^2(\mathbb{R}^\textcolor{red}{3N})}$ wrt. the unknows $\Phi$. This is called **Hartree-Fock** and indeed sometimes considered. However, for many applications a more accurate description is the related **Kohn-Sham density-functional theory (DFT)**, where one additionally modifies the energy expression and considers a minimisation problem
```math
\tag{2}
\Phi_\ast = \mathop{\text{argmin}}_{\substack{\psi_i \in H^1(\mathbb{R}^3) \\ \langle\psi_i, \psi_j\rangle=\delta_{ij} }} \, E_\text{KS}(\Phi)
```
Without going into too many details $E_\text{KS}(\Phi)$ is similar to $\langle \widetilde{\Psi}, \mathcal{H} \widetilde{\Psi}\rangle_{L^2(\mathbb{R}^\textcolor{red}{3N})}$, but not identical. Precisely the **energy functional** $E_\text{KS}(\Phi)$ has the terms
```math
\tag{3}
\begin{aligned}
E_\text{KS}(\Phi) &= \frac12 \sum_{n=1}^N \langle\nabla \psi_n, \nabla \psi_n\rangle
&& \text{\textit{Kinetic energy}} \\
	&+ \int_{\mathbb{R}^3} \frac{\rho_\Phi(r) \rho_\Phi(r')}{|r-r'|} dr dr'
&& \text{\textit{Classical electron-electron repulsion}} \\
	&+ E_\text{xc}(\rho_\Phi) && \text{\textbf{QM: exchange-correlation functional}} \\
	&+ \int_{\mathbb{R}^3} \rho_\Phi(r) V_\text{ext}(r) d r
&& \text{\textit{electron-nuclear attraction}}
\end{aligned}
```
where the external potential describes the Coulombic attraction of electrons
with the nuclei
and the **electron density** is
```math
\rho_\Phi(r) = \sum_{n=1}^N |\psi_n(r)|^2,
```
which is a function $\mathbb{R}^3 \to \mathbb{R}$ and describes the density of electronic charge at every point in space.

"""

# ╔═╡ ccb9d36e-92d7-4865-895e-70aa7b6fe450
md"""
To see the connection to the usual eigenvalue problems we considered, consider the case $N=1$. For a subclass of models (called self-interaction-free DFT) equation (3) then reduces to the first and the last term, i.e.
```math
E_\text{KS}(\psi_1) = \frac12 \langle\nabla \psi_1, \nabla \psi_1\rangle + \int_{\mathbb{R}^3} |\psi_i(r)|^2  V_\text{ext}(r)\, \text{d} r
```
where $ρ_\Phi(r) = |\psi_1(r)|^2$ has been inserted. Using partial integration
on the first term this can be rewritten as
```math
E_\text{KS}(\psi_1) = \int_{\mathbb{R}^3} \psi_i^\ast, \left(-\frac12 \Delta + V_\text{ext} \right) \psi_i,
```
which is the Rayleigh quotient of the Hamiltonian $\mathcal{H} = -\frac12 \Delta + V_\text{ext}$ under the condition $\| \psi_1 \| = 1$. For this setting the minimisation problem (2) is thus nothing else than seeking the lowest-energy state (ground state) of $\mathcal{H}$, i.e. finding the smallest eigenvector $\psi_1$.

In other words problem (2) is a standard eigenvalue problem.
"""

# ╔═╡ 0e1cc724-329e-4487-ac16-57a85ca84502
md"""
For more particles $N > 1$ and general models the situation is more involved and a fully rigorous treatment leads to the result that the stationarity conditions of (2) have the form of a **non-linear eigenvalue problem**
```math
\tag{4}
\left\{
\begin{aligned}
&\left( -\frac12 \Delta + V\left(\rho_\Phi\right) \right) \psi_i = \varepsilon_i \psi_i, \quad \text{for} \ 1 \leq i \leq N \\
V(\rho) = &\, V_\text{ext} + V_H(\rho) + V_\text{xc}(\rho), \\
\rho_\Phi = &\sum_{i=1}^N |\psi_i|^2, \\
\end{aligned}
\right.
```
where the **Hartree potential** $V_H$ and **exchange-correlation potential** $V_\text{xc}$ are
```math
\begin{aligned}
V_H(\rho) &= \nabla_\rho E_H(\rho) & V_\text{xc}(\rho) &= \nabla_\rho E_\text{xc}(\rho) \\
E_H(\rho) &= \int_{\mathbb{R}^3} \frac{\rho(r) \rho(r')}{|r-r'|} dr dr'
\end{aligned}
```
and one often summarises $E_\text{Hxc}(\rho) = E_H(\rho) + E_\text{xc}(\rho)$
and $V_\text{Hxc}(\rho) = V_H(\rho) + V_\text{xc}(\rho)$.
"""

# ╔═╡ 5fe6794f-db25-490d-b210-499547a4fb55
md"""
### Connection to Schrödinger problems (optional)

A more detailed treatment illustrating the derivation of density-functional theory from the many-body Schrödinger problem (1) is given in the lecture notes [Summary of DFT theory](https://michael-herbst.com/teaching/2022-mit-workshop-dftk/2022-mit-workshop-dftk/DFT_Theory.pdf).
"""

# ╔═╡ a8b1bfb4-4c21-4db7-a3ff-b500003d3bce
md"""
## Self-consistent field algorithm
Introducing the **potential-to-density map**
```math
\tag{5}
D(V) = \sum_{i=1}^N |\psi_i|^2
```
where $\psi_i$ are the $N$ lowest-energy eigenfunctions of$\left(-\frac12 \Delta + V\right) \psi_i = \varepsilon_i \psi_i$
allows to write the DFT problem in the short-hand form
```math
\tag{6}
\boxed{\rho = D(V(\rho)),}
```
which is a fixed-point problem in $\rho$, also known as the 
**self-consistent field problem** (SCF problem).
Notice that computing $D(V)$ requires the diagonalisation of the operator
$-\frac12 \Delta + V$, which is usually the most
expensive step when solving the SCF problem.

One usually follows an iterative procedure
to solve the above SCF problem $\rho = D(V(\rho))$.
That is to say that starting from an initial guess $\rho_0$ one then
computes $D(V(\rho_n))$ for a sequence of iterates $\rho_n$
until input and output are close enough.
That is until the residual
```math
\tag{7}
R(\rho_n) = D(V(\rho_n)) - \rho_n
```
is small. More details below, see [Convergence and preconditioning techniques](#Convergence-and-preconditioning-techniques).

Related to this section is also
the discussion in DFTK documentation in the chapter [Introduction to density-functional theory](https://docs.dftk.org/stable/guide/density_functional_theory/).
"""

# ╔═╡ db288ef5-d593-4361-b764-99c36e2ff58a
md"""
### Perodic problems and plane-wave discretisations

It turns out that if $V_\text{ext}$
is a $\mathbb{L}$-periodic potential for a lattice $\mathbb{L}$ and unit cell $\Omega$,
then also $\rho$ and as a result the total potential $V$ are $\mathbb{L}$-periodic enabling a Bloch decomposition for the **Kohn-Sham Hamiltonian** $-\frac12 \Delta + V\left(\rho_\Phi\right)$ from (4).
Following similar arguments to the setting of linear eigenvalue problems discussed in [Periodic problems](https://teaching.matmat.org/error-control/10_Periodic_problems.html) than leads to very similar expressions in (3), (4) and (5) with mainly additional integrals over $k$-points appearing.

For example the potential-to-density map becomes
```math
D(V) = \frac{1}{|\Omega^\ast|} \int_{k\in \overline{\Omega}^\ast} \sum_{n=1}^N
|\psi_{nk}|^2
```
where the $\psi_{nk}$ are for each $k$ the $N$ lowest-energy eigenfunctions of
the Bloch fiber
```math
\tag{8}
\Big(\tfrac12 (-i \nabla + k)^2 + V\Big) \psi_{nk} = ε_{nk} \psi_{nk}.
```
A final modification is that for metallic systems it is often useful to introduce a **smearing temperature** $T$. This changes the minimised energy functional (2) by adding an entropy term and furthermore introduces another modification of the potential-to-density map to give
```math
D(V) = \frac{1}{|\Omega^\ast|} \int_{k\in \overline{\Omega}^\ast} \sum_{n=1}^{\mathcal{N}}
f(\tfrac{ε_{nk} - ε_F}{T}) \ |\psi_{nk}|^2
```
where the **smearing function** is for example the Fermi-Dirac function
$f(x) = \frac{1}{1 + e^x}$
and the number of computed orbitals
$\mathcal{N} \gg N$[^1] and $ε_F$ is chosen to give the correct number of electrons, i.e. to ensure that
```math
\int_{\Omega} D(V) = \int_{\Omega} \frac{1}{|\Omega^\ast|} \int_{k\in \overline{\Omega}^\ast}  f(\tfrac{ε_{nk} - ε_F}{T})\ |\psi_{nk}|^2 = N.
```

To **discretise the problem** we now choose a finite number of ``k``-points, $\mathbb K \subset \overline{\Omega^*}$ as well as at each $k$-point employ a $k$-adapted plane-wave basis set
```math
  \mathbb B^{E_\text{cut}}_{k} = \left  \{ e_G \middle \vert G \in \mathbb L^* , \tfrac1{2} |G+k|^2 \leq E_\text{cut} \right \}
```
to solve (8) as discussed in [Periodic problems](https://teaching.matmat.org/error-control/10_Periodic_problems.html).
Notably after discretisation the density-potential map takes the form
```math
\tag{9}
\boxed{
D(V) = \sum_{n=1}^{\mathcal{N}} \sum_{k \in \mathbb{K}}
f(\tfrac{ε_{nk} - ε_F}{T}) \ |\psi_{nk}|^2
}
```
which is usually employed in practice.

[^1]: In theory even $\mathcal{N} = \infty$, but this is impossible in practice and therefore a modelling choice.
"""

# ╔═╡ fd463525-1c0f-4c86-a05d-6e4e1d4bd61b
md"""To make this expression more tractable, we visualise $f(\frac{ε_{nk} - ε_F}{T})$ for $ε_F = 0$ and various values of $T$:"""

# ╔═╡ 44f1632c-c343-40bd-8cca-a8331b506934
let
	f(x) = 1 / (1 + exp(x))
	p = plot(xlims=(-5, 5), xlabel=L"ε_{nk}", ylabel=L"f(ε_{nk}/T)")
	for T in (1e-3, 1e-1, 0.5)
		plot!(p, x -> f((x - 0) / T); label="T = $T", lw=2)
	end
	p
end

