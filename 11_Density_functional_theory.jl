# ╔═╡ 68925804-b586-4b5f-9153-4b6d088f7068
begin
	# A pseudopotential is what defines the model used for the potential Vext
	pseudopotentials = PseudoFamily("dojo.nc.sr.pbe.v0_4_1.stringent.upf")

	# Define the DFT model using structure, pseudopotential and DFT functional
	model = model_DFT(silicon; pseudopotentials, functionals=PBE())
end;

# ╔═╡ 2cbb7f48-a622-4eb9-b800-0d85496e20cf
md"""
We choose a Monkhorst-Pack kpoint grid with $4 \times 4 \times 4$ $k$-points and a plane-wave basis cutoff $E_\text{cut} = 20$:
"""

# ╔═╡ e49a9f43-8646-40a9-a3ed-d26c6a982610
begin
	kgrid = MonkhorstPack(4, 4, 4)
	basis = PlaneWaveBasis(model; Ecut=20, kgrid)
end

# ╔═╡ 96cbe67d-d3c4-4944-bef6-4018c4db51bb
md"Finally we solve equation (3) using self-consistent field iterations, i.e. aiming to solve the fixed-point problem (6):"

# ╔═╡ 864205b2-c183-4932-852a-1e0a085bba9d
scfres = self_consistent_field(basis; tol=1e-6);

# ╔═╡ b38c886e-152d-424a-b6a2-5c533b6c8f93
md"""
The field `scfres.ρ` now contains the fixed-point density $\rho_ast$ and the field `scfres.ham` the Hamiltonian $-\frac12 \Delta + V(\rho_\ast)$ at this fixed-point density (see also the [DFTK Tutorial](https://docs.dftk.org/stable/guide/tutorial/) for details on the fields of `scfres`).

Since for a fixed $\rho$ $-\frac12 \Delta + V(\rho_\ast)$ is just a linear operator $\mathcal{H}$ on $L^2_\text{per}$ with fibers $\mathcal{H}_k$ we are able to plot its bands, exactly as discussed in [periodic problems](https://teaching.matmat.org/error-control/10_Periodic_problems.html):
"""

# ╔═╡ a35c303a-ed97-4045-a798-687fb8cad88b
