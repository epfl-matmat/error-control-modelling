In the final lectures we will consider the quantum-chemical modelling of materials using Kohn-Sham density-functional theory (DFT) as one example. This model is used on a regular basis (think thousands of times per day on standard supercomputers) to simulate the properties of materials from first princples. That is just from the atomic structure of a material, this approach is able to make predictions about the physical and chemical properties. For designing new catalysts, batteries, solar cells, quantum computing devices, ... this is an invaluable asset.

