### Example: A first DFT calculation of a silicon crystal

In the section on [periodic problems](https://teaching.matmat.org/error-control/10_Periodic_problems.html#Adding-potentials) we considered the computation of the band structure of $-\frac12 \Delta + V$ for a *linear* Hamiltonian (not dependent on $\rho$) and one 1D.

We now extend this to 3D and the DFT computation on a silicon crystal, which is a problem of practical relevance.
"""

# ╔═╡ cb5679b0-b6cc-48f3-b181-c7c595d2b72b
md"""
As the definition of lattices and potentials $V_\text{ext}$ is considerably more infolved for standard systems, we will use the `AtomsBuilder` package where many standard materials systems are readily tabulated.

For example silicon is defined like this on the atomistic level:
"""

# ╔═╡ 54907a13-d519-4689-a64f-b9e4c1b6e888
silicon = bulk(:Si)

# ╔═╡ cdaaf2fa-0e88-4fae-8366-47064648de6f
md"""
This we now model in DFTK using the `PBE` density-functional theory model.

The `functionals` keyword argument defines the structure of the $E_\text{xc}(\rho)$ in the energy functional (3)
